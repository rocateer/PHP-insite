<script type="text/javascript">
	$(function(){
		$('.category_map').find('*').addClass('cv');
		$('.category_li').hover(function(e){
			$('.category_map_back').show();
		})
		$('.category_map').hover(function(e){
			$('.category_map_back').show();
		})
		$('.category_map_back').hover(function(e){
			$('.category_map_back').hide();
		})
	})
	document.getElementsByClassName("gnb_ul")[0].addEventListener("mousemove", function(event) {
		if(!$(event.target).hasClass('category_li') && !$(event.target).hasClass('category_a') && !$(event.target).hasClass('cv')){
			$('.category_map_back').hide();
		}else if($(event.target).hasClass('category_li') && $(event.target).hasClass('category_a') && $(event.target).hasClass('cv')){
			$('.category_map_back').show();
		}
	});
</script>
