나. 제6조 ③항에 의하여 회원의 자격을 상실한 적이 있는 경우. 단, 제6조 ③항에 의한 회원자격상실 후 3년이 경과한 자로서 회사의 회원재가입 승낙을 얻은 경우에는 예외.<br>

