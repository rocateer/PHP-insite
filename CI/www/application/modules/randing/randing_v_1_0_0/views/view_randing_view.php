④ 회사가 제③항에 따라 개정약관을 공지하면서 회원에게 30일 기간 내에 의사표시를 하지 않으면 의사표시가 표명된 것으로 본다는 뜻을 명확하게 공지하였음에도 회원이 명시적으로 거부의 의사표시를 하지 아니한 경우 회원이 개정약관에 동의한 것으로 봅니다.<br>

⑤ 회사가 약관을 개정할 경우에는 그 개정약관은 그 적용일자 이후에 체결되는 계약에만 적용되고 그 이전에 이미 체결된 계약에 대해서는 개정 전의 약관조항이 그대로 적용됩니다. 다만, 이미 계약을 체결한 이용자가 개정약관 조항의 적용을 원하는 뜻을 제③항에 의한 개정약관의 공지기간 내에 타이거에 송신하여 타이거의 동의를 받은 경우에는 개정약관 조항이 적용됩니다.<br>

⑥ 이 약관에서 정하지 아니한 사항과 이 약관의 해석에 관하여는 정부가 제정한 전자상거래 등에서의 소비자보호 지침 및 관계법령 또는 상관례에 따릅니다.<br><br>

제4조 (이용계약 체결)<br><br>

① 이용계약은 회원이 되고자 하는 자(이하 ‘가입신청자’라고 합니다)가 약관의 내용에 대하여 동의를 한 후 회사가 정한 가입양식에 따라 회원가입신청을 하고 회사가 이러한 신청에 대하여 승낙함으로써 체결됩니다.<br>

