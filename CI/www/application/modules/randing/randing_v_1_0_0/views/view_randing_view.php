제2조 (용어의 정의)<br><br>

본 약관에서 사용하는 용어의 정의는 다음과 같습니다.<br>

① ‘타이거’란 회사가 운영하는 가상의 사업장(타이거 웹사이트, 타이거 모바일 어플리케이션)을 말하며, 아울러 가상의 사업장을 통해 제공되는 서비스의 의미로도 사용합니다.<br>

② ‘서비스’란 회사가 타이거에서 제공하는 재화의 공급 및 용역의 이행을 말합니다.<br>

③ ‘이용자’란 타이거에 접속하여 이 약관에 따라 회사가 제공하는 서비스를 받는 회원 및 비회원을 말합니다.<br>

④ ‘회원’이라 함은 ‘일반회원’과 ‘딜러회원’을 포괄하여 말하는 것입니다.<br>

⑤ ‘비회원’이라 함은 회원으로 가입하지 않고 회사가 제공하는 서비스를 이용하는 자를 말합니다.<br>

⑥ ‘구매자’라 함은 서비스 및 물품을 구매하는 자를 말합니다.<br>

⑦ ‘일반회원’이라 함은 회사의 서비스에 접속하여 이 약관에 동의 후 개인정보를 제공하여 회원 등록을 한 자로서, 타이거의 정보를 지속적으로 제공받으며, 타이거에서 제공하는 서비스를 계속적으로 이용할 수 있는 자를 말합니다.<br>

⑧ ‘딜러회원’이라 함은 회사의 서비스에 접속하여 이 약관에 동의 후 개인정보를 제공하여 회원 등록신청을 한 후 타이거에서 회원 등록을 승낙한 자로서 타이거의 정보를 지속적으로 제공받으며, 타이거가 제공하는 서비스를 계속적으로 이용할 수 있는 자를 말합니다.<br>

⑨ ‘구매요청’이라 함은 일반회원이 서비스를 이용함에 있어서 타이거에 차량매입을 위한 정보 등을 제출하는 등의 입찰행위를 의미합니다.<br>

⑩ ‘경매입찰’이라 함은 딜러회원이 서비스를 이용함에 있어서 일반회원에게 차량매도를 위한 정보 등을 타이거에 제출하는 등의 입찰행위를 의미합니다.<br><br>

제3조 (약관의 게시와 개정)<br><br>

① 회사는 이 약관의 내용을 이용자가 알 수 있도록 타이거의 화면에 게시합니다.<br>

② 회사는 전자상거래 등에서의 소비자보호에 관한 법률, 약관의 규제에 관한 법률, 전자문서 및 전자거래 기본법, 전자서명법, 정보통신망 이용촉진 및 정보보호 등에 관한 법률, 소비자 기본법 등 관련법을 위배하지 않는 범위에서 이 약관을 개정할 수 있습니다.<br>

