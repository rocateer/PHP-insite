- 회원의 게시물이 Plan A 서비스와 관련 없는 내용인 경우
- 회원의 게시물이 허위 정보를 포함하고 있는 경우

-게시물 원칙

게시물의 범위는 을 위해서 작성되는 모든 내용에 해당합니다.

1. 회원이 서비스 내에 작성한 여행 정보, 텍스트, 이미지, 동영상, 링크 등의 기타 정보(이하 "게시물")에 대한 책임 및 권리는 게시물을 등록한 회원에게 있습니다.
2. 회사는 회원이 작성한 여행일정, 콘텐츠에 대해서 감시 및 관리할 수 없으며 이에 대해서 책임지지 않습니다. 회사는 회원이 등록하는 여행일정, 게시물의 신뢰성, 진실성, 정확성 등에 대해서 책임지지 않으며 보증하지 않습니다.
3. 서비스에 대한 저작권 및 지적재산권, 회사가 작성한 여행일정, 게시물의 저작권은 회사에 귀속됩니다. 단, 회원이 단독 또는 공동으로 작성한 게시물 및 제휴계약에 따라 제공된 저작물 등은 제외합니다.
4. 회원은 자신이 서비스 내에 게시한 게시물을 회사가 국내·외에서 다음 목적으로 사용하는 것을 허락합니다.
