Plan A 에 오신 것을 진심으로 환영합니다. Plan A 주식회사(이하 “회사”)는 여행일정 관리 모바일 어플리케이션을 제공하며, 본 이용약관은 도메인 www.planaapp.com의 전체 웹 사이트, Plan A 모바일 어플리케이션 및 회사와 회원 사이에 적용됩니다. Plan A 서비스를 사용하기 전에 본 이용약관(이하 “약관”)의 전체 내용을 주의해서 읽어 보십시오. 회사는 아래의 내용에 동의하시는 회원에 한하여 정상적인 서비스를 이용할 수 있습니다. 내용에 동의하지 않으면 어플리케이션 서비스 사용이 불가능합니다.


제 1 조 목적

본 약관은 회사가 모바일 어플리케이션을 통해 제공하는 사진, 동영상 저장, 공유 및 기타 정보서비스(이하 “서비스”)와 관련하여, 회사와 회원 간의 권리와 의무, 책임사항과 회원의 서비스 이용 절차에 관한 사항을 규정하는 것을 목적으로 합니다.

제 2 조 약관의 게시와 효력, 개정

가. 회사는 서비스를 이용하고자 하는 사람 및 회원이 본 약관에 대해 알 수 있도록 본 약관을 서비스가 제공되는 인터넷 사이트(www.planaapp.com) 화면에 게시합니다.
나. 회사는 본 약관을 관련법령을 위배하지 않는 범위에서 수시로 개정할 수 있습니다. 개정된 약관은 온라인에 명시됨으로써 효력을 발휘하며 전항과 같은 방식으로 공지합니다.
다. 본 약관에 동의하는 것은 서비스가 제공되는 인터넷 사이트(www.planaapp.com)에 정기적으로 방문하여 본 약관의 변경사항을 확인하는 것에 동의함을 의미합니다. 변경된 약관에 대한 정보를 알지 못해 발생하는 이용자의 피해는 회사에서 책임지지 않습니다.
라. 회사가 본 약관을 개정하는 경우에는 개정된 약관의 적용일자 및 개정사유를 명시하여 그 적용일자 7일 이전부터 적용일자 전일까지 위 ‘가’항의 방법으로 공지합니다.
마. 회원이 개정된 약관에 동의하지 않는 경우 회원은 서비스 탈퇴(해지)를 할 수 있으며, 변경된 약관의 효력 발생일로부터 7일 이후에도 거부의사를 표시하지 아니하고 서비스를 계속 이용할 경우 개정된 약관에 동의한 것으로 간주합니다.
바. 본 약관은 회사와 회원 간에 성립되는 서비스이용계약의 기본 약정입니다. 회사는 필요한 경우 특정 서비스에 관하여 적용될 사항(이하 "개별약관")을 정하여 미리 공지할 수 있습니다. 회원이 이러한 개별 약관에 동의하고 특정 서비스를 이용하는 경우 그 개별 약관이 우선적으로 적용되며 본 약관은 보충적인 효력을 갖습니다. 개별 약관의 변경에 관해서는 위 ‘나’항을 준용합니다.

제 3 조 약관의 해석과 예외 준칙

가. 회사는 회사가 제공하는 개별 서비스에 대해 별도의 이용약관 및 정책을 둘 수 있으며, 해당 내용이 이 약관과 상충할 경우 개별 서비스의 이용약관을 우선적으로 적용합니다.
나. 본 약관에 명시되지 않은 사항은 전기통신기본법, 전기통신사업법 및 기타 관련법령의 규정 및 회사가 정한 서비스의 세부이용지침 등의 규정에 따릅니다.


제 4 조 용어의 정의

가. “서비스”란 그것이 구현되는 단말기(PC, TV, 휴대형단말기 등의 각종 유무선 장치를 포함)와 상관없이 회원이 이용할 수 있는 Android, iOS 기반 모바일 어플리케이션 및 웹 사이트(www.planaapp.com) 및 관련 제반 서비스를 의미합니다.
나. “이용자”란 회사의 서비스에 접속한 모든 회원 및 비회원을 의미합니다.
다. “회원”이란 회사의 서비스에 접속하여 본 약관에 따라 회사와 이용계약을 체결하고 회사가 제공하는 서비스를 이용하는 이용자를 말합니다.
라. “이메일 아이디(Email ID)”란 회사의 회원 식별과 회원의 서비스 이용을 위하여 회원이 정하고 회사가 승인하는 문자와 숫자의 조합을 의미합니다.
마. “비밀번호”란 회원의 개인정보 확인을 위해서 회원이 정한 문자 또는 숫자의 조합을 의미합니다.
바. “도메인”이란 회원의 서비스 이용을 위하여 회원이 신청하고 회사가 부여한 고유한 인터넷 주소를 의미합니다. 회사는 서비스에 따라 별도의 도메인 주소를 추가적으로 제공합니다.
사. "게시물"이라 함은 "회원"이 "서비스"를 이용함에 있어 "서비스상"에 게시한 부호ㆍ문자ㆍ음성ㆍ음향ㆍ화상ㆍ동영상 등의 정보 형태의 글, 사진, 동영상 및 각종 파일과 링크 등을 의미합니다.
아. “유료서비스”란 회사가 유료로 제공하는 여행상품 광고 구축·관리 서비스 및 제반 서비스를 의미합니다.
자. “제휴업체”란 회사와 별도의 계약을 맺고 회사와 함께 혹은 회사로부터 위탁을 받아 서비스를 제공하는 개인이나 단체 또는 회사를 말합니다.

제 5 조 이용계약의 체결

가. 이용계약 성립 시기는 회원으로 등록하여 서비스를 이용하려는 사람(이하 “가입신청자”)이 본 약관에 동의하고 가입을 신청한 것에 대하여 회사가 승낙하고 해당 가입신청자가 서비스에 로그인하는 것이 가능해진 시점을 기준으로 합니다.
나. 가입신청자는 서비스 가입신청 시 본 약관을 읽고 아래에 있는 "서비스 약관을 읽었으며 이에 동의합니다"에 체크하는 것으로 본 약관에 대한 동의 의사 표시를 합니다. 또한 페이스북으로 가입 시, 페이스북으로 시작 버튼을 눌러 동의 의사 표시를 합니다.
다. 약관을 읽지 않아서 발생할 수 있는 피해에 대한 책임은 전적으로 회원에게 있습니다.
라. 이용계약의 성립 시기는 회사가 가입 완료를 신청절차 상에서 표시한 시점으로 합니다.
마. 회사는 회사 정책에 따라 회원을 등급별로 구분하여 이용시간, 이용횟수, 서비스 메뉴 등을 세분하고 이용에 차등을 둘 수 있습니다.

-서비스이용신청

1. 가입신청자는 아래 사항을 회사가 온라인으로 제공하는 가입신청양식에 따라 기재하여야 합니다. 가입신청 시 기재사항은 가입신청자가 개인인지 기업 인지에 따라 구분됩니다.
A. 개인 회원의 경우 필수기재항목

- 필수 수집항목 :, 비밀번호, 이메일 아이디, 이용자의 전화번호, 성별, 생년월일, 활동지역, 관심사
- 선택 수집항목 : 실명, 자기소개, 자신의 홈페이지 주소 URL, 연락처(휴대폰 번호), 거주지, 자주 이용하는 공항

B. 기업회원인 경우 필수기재항목

- 필수 수집항목 : 회사이름, 회사 카테고리, 활동지역
- 선택 수집항목 : 회사 웹사이트, 회사소개, 거주지, 자주 이용하는 공항

2. 가입신청자는 반드시 본인의 정보를 거짓 없이 기재하여야 합니다. 본인의 정보로 등록하지 않은 회원은 법적인 보호를 받을 수 없으며, 서비스 이용 시에 불이익을 당할 수 있습니다. 또한 서비스에서의 어떠한 권리도 주장할 수 없습니다.
3. 회사는 회원가입시의 실명확인을 위하여 실명확인 조치를 할 수 있습니다.
4. 타인의 명의(이름, 회사명, 단체명)를 도용하여 이용신청을 한 회원의 모든 이메일 아이디는 삭제되며, 관계법령에 따라 처벌을 받을 수 있습니다.


-이용신청의 승낙

1. 회사는 가입신청자의 신청에 대하여 서비스 이용을 승낙함을 원칙으로 합니다. 다만, 회사는 아래에 해당하는 신청에 대하여는 승낙을 하지 않거나 사후에 이용계약을 해지할 수 있습니다.

- 가입신청자가 본 약관에 의하여 이전에 회원자격을 상실한 적이 있는 경우. 다만 회사의 회원 재가입 승낙을 얻은 경우에는 예외로 함.
- 제3자의 이메일 주소를 이용하여 신청한 경우
- 허위의 정보를 기재하거나 회사가 필수적으로 입력을 요청한 부분을 기재하지 않은 경우
- 부정한 용도로 서비스를 이용하고자 하는 경우
- 이용자의 귀책사유로 인하여 승인이 불가능하거나 기타 규정한 제반 사항을 위반하며 신청하는 경우
- 회사의 정책에 적합하지 않는 회원으로 판단되는 경우나 서비스 제공이 곤란한 경우
- 회원의 이용 목적이나 서비스 이용 방법이 회사의 재산권이나 영업권을 침해하거나 침해할 우려가 있는 경우
- 비정상적인 방법을 통하여 이메일 아이디 및 도메인을 대량으로 생성하는 행위를 하는 경우

-서비스의 위탁

회사는 필요하다고 인정하는 경우 서비스의 일부를 제휴 업체에 위탁할 수 있습니다.

제 6 조 개인정보보호의무

가. 회사는 정보통신망법 등 관계 법령이 정하는 바에 따라 회원의 개인정보를 보호하기 위해 노력합니다. 개인정보의 보호 및 사용에 대해서는 관련법 및 회사의 개인정보취급방침이 적용됩니다. 다만, 회사의 공식 사이트 이외의 링크된 사이트에서는 회사의 개인정보취급방침이 적용되지 않습니다.
나. 회사가 서비스를 중단하거나 회원이 개인정보 제공 동의를 철회한 경우 회사는 신속하게 회원의 개인정보를 파기합니다. 단, 전자상거래 등에서의 소비자 보호에 관한 법률 등 관련 법률에서 정하는 바에 따라 특정 정보는 보관할 수 있습니다.
다. 회사는 서비스 개선 및 회원에게 서비스 소개 등의 목적을 위해 회원의 동의 하에 관계 법령에서 정하는 바에 따라 추가적인 개인정보를 수집할 수 있습니다.
라. 회사는 법률에 특별한 규정이 있는 경우를 제외하고는 회원의 별도 동의 없이 회원의 계정정보를 포함한 일체의 개인정보를 제3자에게 공개하거나 제공하지 않습니다.
마. 회사는 향후 제공하는 서비스에서 회원의 편의를 위해서 회원의 계정 정보를 사용할 수 있도록 링크 및 기타 방법을 제공할 수 있습니다.

제 7 조 회원정보의 변경

가. 회원은 개인정보관리화면을 통하여 언제든지 본인의 개인정보를 열람하고 수정할 수 있습니다. 다만, 서비스 관리를 위해 필요한 실명, 이메일 아이디(회사에 직접 문의 시 변경 가능), 활동지역 등은 수정이 불가능합니다.
나. 회원은 가입신청 시 기재한 사항이 변경되었을 경우 온라인으로 수정을 하거나 기타 방법으로 회사에게 그 변경사항을 알려야 합니다.
다. 전항의 변경사항을 회사에 알리지 않아 발생한 불이익에 대하여 회사는 책임지지 않습니다.
라. 기타 이용자 개인정보 관리 및 변경 등에 관한 사항은 서비스 별 안내에서 정하는 바를 따릅니다.

제 8 조 회원의 이메일 아이디 및 비밀번호 관리에 대한 의무

