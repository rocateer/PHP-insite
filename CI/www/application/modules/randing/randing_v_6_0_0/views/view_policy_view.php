제 13 조 게시물 정책

Plan A는 회원의 모든 콘텐츠를 존중합니다. 회원이 성의를 다해 게시한 여행 일정, 사진, 동영상, 글에 대해서 Plan A는 사전통지 없이 삭제하거나 수정하지 않습니다. 그러나 다른 회원에게 물리적·정신적으로 해를 가하거나 저작권을 침해하는 일정에 대해서는 게시물 삭제 및 해당 회원의 서비스 이용 제한 등의 적법한 조치를 취할 수 있습니다.

