//모달 백그라운드 스크롤 막기
var scrollTop;

$.lockBody = function() {

if(window.pageYOffset) {
  scrollTop = window.pageYOffset;
  $(".wrap").css({
    top: - (scrollTop)
  });
}

$('html, body').css({
  height: "100%",
  overflow: "hidden"
});
};

$.unlockBody = function() {
$('html, body').css({
  height: "",
  overflow: ""
});

$(".wrap").css({
  top: ''
});

window.scrollTo(0, scrollTop);
window.setTimeout(function () {
  scrollTop = null;
}, 0);

};

$(document).ready(function(){

//해당 섹션으로 스크롤, 메뉴 active
var section_arr = ["#section_home","#section_about","#section_partership","#section_qa"];
var section_distance = [];
var header_height = 64;

for (var i = 0; i < section_arr.length; i++) {
  section_distance.push(eval("$(" + "section_arr[i]" + ").offset().top"));
}

$(window).on('scroll', function(){
  var scroll = $(window).scrollTop() + header_height;
  $('.menu a').removeClass('active');

  for(var i = 0; i < section_arr.length; i++){

    var section_end = section_distance[i] + $(section_arr[i]).outerHeight();

    if(i == 0){ //첫번째 섹션
       if (scroll >= 64 && scroll < section_end) {
         $('.menu a').eq(i).addClass('active');
       }
    } else {
      if (scroll >= section_distance[i] && scroll < section_end) {
       $('.menu a').eq(i).addClass('active');
      }
    }
  }
});

//스크롤 애니메이션
$('.menu a').click(function(event) {
  event.preventDefault();
  var link = $(this).attr("href")
  $("html, body").animate({ scrollTop: $(link).offset().top - 61 }, 300);
});

